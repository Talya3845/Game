public class Square {
    private int column;     //x
    private int row;        //y

    public Square() {
        column=0;
        row=0;
    }

    public int getColumn(){
        return column;
    }
    public int getRow(){
        return row;
    }

    public void setColumn(int column){
        this.column = column;
    }
    public void setRow(int row){
        this.row = row;
    }


}



